// dart list properties
void main(){
  List<int> number=[1,2,3,4,5,6];
  print(number);
  // first- show first element
  print(number.first);
  // show last element
  print(number.last);
  //show list length- item number
  print(number.length);
  // list item reversed
  print(number.reversed);
  // list no item show- true , list  item show - false
  print(number.isEmpty);
  // list show item - true , list no item - true
  print(number.isNotEmpty);
}